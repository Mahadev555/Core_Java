public class java_loops {


    public static void main(String[] args) {
        //for loop
        for (int i = 0;i<11;i++){
            System.out.print(i +" ");
        }

        //While loop
        int i = 0;
        while (i<11){
            System.out.println(i);
            i++;
        }

        //do while
        int o = 0;
        do{
            System.out.println(o);
            o++;
        }
        while (o<11) ;

    }

}
